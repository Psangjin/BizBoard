-- 알림 본문
CREATE TABLE INFORM (
  INFORM_ID              NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  INFORM_TYPE            VARCHAR2(50)      NOT NULL,     -- PROJECT_EVENT | TASK_EVENT | NOTICE_EVENT
  INFORM_MEMO            CLOB,
  INFORM_OCCURREDATETIME TIMESTAMP(6)      DEFAULT SYSTIMESTAMP NOT NULL
);
-- 알림 수신자
CREATE TABLE INFORMRECEIVER (
  INFORMRECEIVER_ID            NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  INFORMRECEIVER_INFORM_ID_REF NUMBER        NOT NULL,   -- FK → INFORM.INFORM_ID
  INFORMRECEIVER_EMP_ID_REF    VARCHAR2(100) NOT NULL,   -- 유저 ID (String)
  INFORMRECEIVER_READINGSTATUS NUMBER(1)     DEFAULT 0 NOT NULL, -- 0 미읽음 / 1 읽음
  INFORMRECEIVER_READ_AT       TIMESTAMP(6)
  --CONSTRAINT FK_IR_INFORM FOREIGN KEY (INFORMRECEIVER_INFORM_ID_REF)
    --REFERENCES INFORM (INFORM_ID)
);

CREATE SEQUENCE SEQ_INFORM START WITH 1;